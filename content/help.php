<div id="visa" class="help_content">
	<h3>VISA</h3>
	<p>US Visa is important for you to get into the States legally.</p>
	<p>Remember this before applying for a VISA: </p>
	<ol>
	<li>For students: Get your SEVIS done,you would need the receipt during your interview/VISA Processing</li>
	<li>Finance proof</li>
	</ol>
	<p>Tips: Always, have your Passport, I-20 around when you are at a port of entry.</p>
	<p>For current students: Before going travelling outside the States, there is a travel advisory seminar in the Iowa State University that I would recommend you to go. You should also check your I-20 and get a travel signature if necessary</p>
	<p>For more questions, ISSO would be the best source for you to clarify your doubts. <a class="btn btn-lg btn-primary" href='https://www.isso.iastate.edu/' target="_blank">View ISSO Website</a></p>

